    public void loadFont() {
        GraphicsEnvironment graphicsEnvironment = GraphicsEnvironment.getLocalGraphicsEnvironment();
        Font[] fonts = graphicsEnvironment.getAllFonts();
        for (Font f : fonts) {
            jComboBox1.addItem(f.getFontName());
        }
    }
    public void pick() throws Exception {
        if (f.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            File file = f.getSelectedFile();
            FileInputStream fis = new FileInputStream(file);
            BufferedReader rd = new BufferedReader(new InputStreamReader(fis, StandardCharsets.UTF_8));
            String arr = "";
            String arr1 = "";
            while ((arr = rd.readLine()) != null) {
                arr1 += arr;
            }
            txtWrite.setText(arr1);
        } else {
            sb.append("No file is selected");
        }
    }

