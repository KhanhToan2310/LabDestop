    private void FindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FindActionPerformed
        removeHighlight(txtWrite);
        if (!jTextField1.getText().equalsIgnoreCase("")) {
            try {
                Highlighter hilite = txtWrite.getHighlighter();
                Document doc = txtWrite.getDocument();
                String text = doc.getText(0, doc.getLength());
                int index = 0;
                while ((index = text.toUpperCase().indexOf(jTextField1.getText().toUpperCase(), index)) >= 0) {
                    hilite.addHighlight(index, index + jTextField1.getText().toUpperCase().length(), highlightWord);
                    index += jTextField1.getText().toUpperCase().length();
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_FindActionPerformed
    class FindWord extends DefaultHighlighter.DefaultHighlightPainter {

        public FindWord(Color color) {
            super(color);
        }
    }

    public void removeHighlight(JTextComponent textComponent) {
        Highlighter hilite = textComponent.getHighlighter();
        Highlighter.Highlight[] hilites = hilite.getHighlights();
        for (int i = 0; i < hilites.length; i++) {
            if (hilites[i].getPainter() instanceof FindWord) {
                hilite.removeHighlight(hilites[i]);
            }
        }
    }

